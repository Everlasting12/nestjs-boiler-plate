/**
 * Function to process user signup data by hitting an API endpoint
 * @param {Array} users - Array of user objects with email, name, and password
 */
async function processUserSignups(users) {
  const apiUrl = 'http://localhost:5000/api/v1/auth/sign-up';

  for (const user of users) {
    try {
      const response = await fetch(apiUrl, {
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
        },
        body: JSON.stringify({
          email: user.email,
          password: user.password,
          name: user.name,
        }),
      });

      if (!response.ok) {
        const errorData = await response.json();
        throw new Error(`HTTP ${response.status}: ${errorData.message || 'Unknown error'}`);
      }

      const responseData = await response.json();
      console.log("User ${user.email} signed up successfully:", responseData);
    } catch (error) {
      console.error("Error signing up user ${user.email}:", error.message);
    }
  }
}

